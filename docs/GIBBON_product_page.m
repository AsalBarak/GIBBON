%% Summary of the GIBBON toolbox
% GIBBON is an open-source MATLAB toolbox by Kevin M. Moerman and includes
% an array of image and geometry visualization and processing tools and is
% interfaced with free open source software such as TetGen, for robust
% tetrahedral meshing, and FEBio for finite element analysis. The
% combination provides a highly flexible image-based modelling environment
% and enables advanced inverse finite element analysis.

